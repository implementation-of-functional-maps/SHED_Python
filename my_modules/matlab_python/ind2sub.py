def _(siz, IND): # %d, %d # 入力がどの型か記録したほうがよい.
    col_out = (IND) // siz
    row_out = (IND) %  siz
    #-----------------------------------------------------------------
    #                       Return
    #-----------------------------------------------------------------
    return [row_out, col_out]

